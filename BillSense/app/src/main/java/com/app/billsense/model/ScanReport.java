package com.app.billsense.model;

public class ScanReport {
    String id, userId, imageUrl, model, status, date;

    public ScanReport() {
    }

    public ScanReport(String id, String userId, String imageUrl, String model, String status, String date) {
        this.id = id;
        this.userId = userId;
        this.imageUrl = imageUrl;
        this.model = model;
        this.status = status;
        this.date = date;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
